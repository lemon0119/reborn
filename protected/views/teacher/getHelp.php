        <h2>1、系统登录</h2>
        <h3> 1、 验证端口</h3>
        <p>&nbsp;&nbsp;&nbsp&nbsp;登录之前必须要验证两个端口，否则有些功能无法正常使用。</p>
        <p>&nbsp;&nbsp;&nbsp;&nbsp第一步：输入亚伟速录教学平台网址，并在地址栏中修改网址，将 “/”及后面的内容删除。然后添加“：8443”，访问该网址。
            例：将https://192.168.0.157/reborn/index.php?r=user/login&exit=1
        修改为：https://192.168.0.157：8443。</p>
       <p> &nbsp;&nbsp;&nbsp;&nbsp第二步：输入亚伟速录教学平台网址，并在地址栏中修改网址，将 “/”及后面的内容删除。然后添加“：9001”，访问该网址。
        例：将https://192.168.0.157/reborn/index.php?r=user/login&exit=1
        修改为：https://192.168.0.157：9001。</p>
