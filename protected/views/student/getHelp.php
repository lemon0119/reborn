        <h2>1、系统登录</h2>
        <h3> 1、 验证端口</h3>
        <p>&nbsp;&nbsp;&nbsp&nbsp;登录之前必须要验证两个端口，否则有些功能无法正常使用。</p>
        <p>&nbsp;&nbsp;&nbsp;&nbsp第一步：输入亚伟速录教学平台网址，并在地址栏中修改网址，将 “/”及后面的内容删除。然后添加“：8443”，访问该网址。
            例：将https://192.168.0.157/reborn/index.php?r=user/login&exit=1
        修改为：https://192.168.0.157：8443。</p>
       <p> &nbsp;&nbsp;&nbsp;&nbsp第二步：输入亚伟速录教学平台网址，并在地址栏中修改网址，将 “/”及后面的内容删除。然后添加“：9001”，访问该网址。
        例：将https://192.168.0.157/reborn/index.php?r=user/login&exit=1
        修改为：https://192.168.0.157：9001。</p>
       <h3> 2、 登录</h3>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第一步：在火狐浏览器地址栏输入亚伟速录教学平台网址，进入网站首页。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/sa.png" style="">
       </div>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第二步：在系统登录界面输入用户名和密码（根据不同的用户名和密码，可以进入不同的系统操作平台），此处选择身份为学生</p>
       <div style="text-align: center">
       <img src="./img/UI_help/sb.png" style="">
       </div>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第三步：点击登录，来到“欢迎来到亚伟速录教学平台！”界面。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/sc.png" style="">
       </div>
       <h2> 2、 菜单栏</h2>
       <p>&nbsp;&nbsp;&nbsp&nbsp;该界面上面一栏为菜单栏，可以对教学平台进行所有的操作。 菜单栏包括亚伟速录/兜秘图标、课堂、练习、作业、考试、统计、百科、课程表和学生姓名，以下做详细介绍。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/sd.png" style="">
       </div>
       <h2> 3、 亚伟速录/兜秘图标</h2>
       <p>&nbsp;&nbsp;&nbsp&nbsp;点击第一个功能按钮，就会转到“欢迎来到亚伟速录教学平台！”界面。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/se.png" style="">
       </div>
       <h2> 4、课堂</h2>
       <h3> 4.1、课件</h3>
       <p>&nbsp;&nbsp;&nbsp&nbsp;在课堂中学生可以看到老师所播放的各种类型的课件。</p>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第一步：点击课堂，进入在线课堂，老师端将显示此学生已在线，学生端显示学生的信息和当前所上课时。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/sf.png" style="">
       </div>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第二步：老师端可发布直播视频，视频，图片，文本，PPT，音频，课堂练习。学生端接收到不同的上课资源，但是学生不可以调整课件的播放顺序。</p>
       
