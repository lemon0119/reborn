       <h3> 4.2、课堂练习</h3>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第一步：学生端接收课堂练习的通知，点击确定进入练习页面。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/sm.png" style="">
       </div>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第二步：在课堂练习页面，学生可以进行作答看打练习、听打练习、健打练习。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/sn.png" style="">
       </div>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第三步：学生在练习的过程中如果不满意自己的练习结果可以点击进行“再来一遍”按钮，选择重新再做一遍。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/so.png" style="">
       </div>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第四步：学生在练习的过程中可以通过点击“上一题”、“下一题”按钮，或者通过点击题目下拉框，调整做题进度。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/sp.png" style="">
       </div>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第五步：点击“关闭”按钮返回到课堂页面。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/sq.png" style="">
       </div>
       <p>&nbsp;&nbsp;&nbsp&nbsp;课堂问答：点击“课堂问答”，文本框中输入内容，点击“发送”按钮，同一个班级的老师与学生都可以看到文本内容。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/sr.png" style="">
       </div>
       <h2>5 、练习</h2>
       <p>&nbsp;&nbsp;&nbsp&nbsp;点击练习，显示练习界面学。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/ss.png" style="">
       </div>
       <h3>5.1、自主练习</h3>
       <p>&nbsp;&nbsp;&nbsp&nbsp;学生点击自主练习进入自主练习页面，学生可以自由练习。练习的过程中，学生击键的各项数据会被实时的显示出来。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/st.png" style="">
       </div>
       <h3>5.2、课时练习</h3>
       <p>&nbsp;&nbsp;&nbsp&nbsp;学生选择相应的课时，做老师发布的课堂练习。该练习是老师在上课时发布的。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/su.png" style="">
       </div>
       <h2>6 、作业</h2>
       <p>&nbsp;&nbsp;&nbsp&nbsp;点击作业，显示老师发布的作业。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/sv.png" style="">
       </div>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第一步：点击操作下的图标，进入课堂作业界面，点击选择左侧题目开始做作业，完成后点击提交，提交后不可再改。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/sw.png" style="">
       </div>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第二步：提交成功后返回到作业主界面，点击预览图标查看作答情况。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/sx.png" style="">
       </div>
       <h2>7、考试</h2>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第一步：点击考试，显示发布的试卷信息，已提交的试卷可查看作答情况，不可再次作答。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/sy.png" style="">
       </div>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第二步：点击“进入”按钮进入考试。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/sz.png" style="">
       </div>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第三步： 点击选择左侧题目开始做题，注意考试剩余时间，完成后点击提交，提交后不可再改；考试时间到了也将自动提交。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/sza.png" style="">
       </div>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第四步：提交之后回到考试页面，学生可点击“查看答案”，来查看试卷答案；还可看到老师批阅试卷的结果。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/szb.png" style="">
       </div>
       <h2>8、统计</h2>
       <p>&nbsp;&nbsp;&nbsp&nbsp;学生在统计中可以直观的看到所有学生和平均成绩在作业、练习、考试中答题的各项数据统计信息，还可以将数据统计表导出到电脑中。</p>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第一步：点击统计，进入统计页面，选择左侧要查看的统计条目。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/szc.png" style="">
       </div>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第二步：点击作业，选择查看的相应课时的作业，点击题目显示各个数据的平均值。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/szd.png" style="">
       </div>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第三步：点击学生姓名查看各个学生的数据与平均值的对比。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/sze.png" style="">
       </div>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第四步：点击导出按钮可以将统计表格以excel的格式导出到电脑中。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/szf.png" style="">
       </div>
       <h2>9、百科</h2>
       <p>&nbsp;&nbsp;&nbsp&nbsp;学生在速记百科中可以了解到关于亚伟速录的历史、荣誉、国际速联大赛等相关知识。</p>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第一步：点击顶部导航栏的速记百科，进入速记百科页面。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/szg.png" style="">
       </div>
       <p>&nbsp;&nbsp;&nbsp&nbsp;第二步：在速记百科页面学生可以通过点击左侧导航栏的目录来查看速记百科的内容，也可以通过速记百科内容中的滚动条来查看速记百科的内容。 </p>
       <div style="text-align: center">
       <img src="./img/UI_help/szh.png" style="">
       </div>
       <h2>10、课程表</h2>
       <p>&nbsp;&nbsp;&nbsp&nbsp;在课程表中学生可以查看自己的课程表，但是不能对课程表做出修改</p>
       <p>&nbsp;&nbsp;&nbsp&nbsp;点击课程表按钮进入课程表页面。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/szi.png" style="">
       </div>
       <h2>11、学生姓名</h2>
       <p>&nbsp;&nbsp;&nbsp&nbsp;点击个人设置按钮，进入设置界面，在该页面中学生可以对自己的个人信息进行修改。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/szj.png" style="">
       </div>
       <h3>11.1、个人设置</h3>
       <p>&nbsp;&nbsp;&nbsp&nbsp;点击个人设置按钮，进入设置界面，在该页面中学生可以对自己的个人信息进行修改。</p>
       <div style="text-align: center">
       <img src="./img/UI_help/szk.png" style="">
       </div>
       <h3>11.2、退出</h3>
       <p>&nbsp;&nbsp;&nbsp&nbsp;点击退出登录，则会返回到最初的用户登录页面。</p>
