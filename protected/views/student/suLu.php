        <h3 style="text-align: center;">速录机从立项到发展的艰难经历</h3>
        <h5>八十年代末期，我接到一位美国友人詹妮芬女士的信函，在信函中讲美国速记协会刊登了一篇文章，文章谈及到“美国司法代表团到中国进行一次全面的交流活动”，当时与中国的法院系统相关单位做了全面接触，唯独随团访问中国的两名资深速记师没有找到计算机速记的交流对象，他们在文章中得出了这样一个结论“中国没有计算机速记”。但是，这位美国友人詹妮芬女士知道中国有速记，而且还知道我就是从事这方面研究的专家，她在信中讲“来函的目的是希望我能够在国内搞一些计算机速记方面的研究，并发表相关文章和美国速记界交流”，我看到了这封信函后，马上以北京市速记协会理事长的身份向最高法院提出了关于速录机能够解决庭记录审现代化的建议，最高人民法院办公厅及时给予我答复，我记得答复是这样写的，“如果您能够研究出速录机，并能够记录庭审内容，我们愿意在法院试用并推广”，这个回复给我提供了研发中文速录机的动力。我翻出五十年代开始研究机械速记的所有资料，日以继夜的不断研究，又不断的推翻，毕竟中国人还没有人从事过这方面的研究，没有一丝一毫借鉴的可能，将近用了两年的时间，最终整合出一套完整的速录编码方案，一只手能够打出一个完整的汉语拼音音节，两只手同时并击可以打出两个不相同的汉语音节，即两个汉字，通过这种方式操作，速度大大的提高了，而击键的频度却大大的降低了，这个想法是我从左右手书法得到的启发。</h5>
        <h5>人们常说一心不能两用，但是人们只要经过刻苦训练，就能够启发人类无尽的潜能，达到两只手同时敲击一个词组，我当时认为是可以实现的。当然，这只是理论的研究和自己的想法，如何实现这个方案，我曾经亲自试验过多种的方式，年纪虽然大，反应也迟钝了，经过实践演练，我认为它是能够成功的。</h5>
        <h5>方案有了，真要把它制造出来谈何容易？因为我多年从事教学工作，和一些企业家、实业家都疏于沟通，没有这样的相关朋友。但是，如何找到投资来开发这个产品，是我当时最费脑筋的事情，有几个好朋友听说我的方案后，愿意帮忙找投资，但是由于当时计算机时代还没有真正的来临，人们对我这个方案总是感觉有一些阳春白雪。我曾骑着自行车到过中关村的多家企业进行过洽谈，也曾发一些信件给有关部门呼吁，大都无功而返，最后终于我们找到了晓军公司投资。93年立项，开始招兵买马，在中关村化学所开始速录机的开发工作。每天几乎都在开会，甚至有的会议要开到深夜两三点钟，虽然那时候我已经将近80高龄，但是我也始终跟那些年轻人一块儿摸爬滚打。94年终于把速录机研究出来了，94年5月19号那天，在北京展览馆向全国公布速录机这一科技成果。但是，由于人们对新产品速录机的认识不足，推广起来难度极大。94年到96年公司投入了将近900万元来进行研发推广，始终没有收到良好的效果，由于中关村的房租太贵，在万般无奈的情况下，我们撤出了中关村，撤出时公司帐面上只剩下仅仅的62元。</h5>
        <h5>速录机在推广当中虽然非常艰苦，但是我们始终受到了法院系统的高度重视。由于当时法院系统计算机还没有普及起来，推广起来难度很大。但是，最高人民法院办公厅的鲍圣庆同志曾亲自来我公司进行实地调查，并亲自参加学习，以求证亚伟中文速录机在法院庭审记录中的实用性，他的行动鼓舞了我们，同时我们也在不断的改进我们的软件系统。96年最高人民法院办公厅把亚伟中文速录机列入了法院工作的2010年远景规划，这无疑是对亚伟中文速录机的充分认可，也增强了我们推广速录机、培养速录人才的信心。97年开始，最高人民法院及各地方法院发了十几个文件，推广亚伟中文速录机，尤其是在98年，肖扬院长亲笔批示，“此事甚好，应积极推进，各级法院领导应大力支持，开创法院现代化（包括庭审记录、办案、办公现代化）新局面”，这无疑是一股春风，温暖了我们每个从事速录机研发人员的心，尤其是我，感到了非常荣幸。从最高法院技术局局长张力理同志、副局长全国恩同志以及计算机处王德进处长都对亚伟中文速录机表示了亲切的关怀。</h5>
        <h5>从96年开始到现在，全国3000多家法院几乎都使用亚伟中文速录机在法庭中进行庭审记录，速录机终于从困境中走了出来。</h5>
