        <h3 style="text-align: center;">亚伟速录荣誉历程</h3>
        <h5>1993年，著名速记专家唐亚伟教授发明了亚伟中文速录机。北京晓军办公设备有限公司集各学科专家数十人，秉承自主创新的精神，没用国家一分钱，耗巨资将其研制成功。</h5>
        <h5>亚伟中文速录机的正常输入速度为每分钟220字，极限速度则达到每分钟520字。</h5>
        <h5>1994年11月24日，亚伟中文速录机通过了劳动部职业技能与技术鉴定。专家一致认为该产品属于国内首创，达到国际先进水平。</h5>
        <h5>1996年3月18日，亚伟中文速录机被亚列入国家火炬计划项目。</h5>
        <h5>1997年，最高人民法院发文在全国法院系统推广使用亚伟中文速录机实现庭审记录计算机化</h5>
        <h5>2002年，全国人大信息中心正式组建专职速录师队伍，服务于全国人大常委会每2个月一次的会议以及平常的委员长会议和日常办公会议等的现场记录。</h5>
        <h5>2003年8月18日，国家劳动和社会保障部颁布了《速录师》国家职业标准，将速录师纳入了国家职业体系，并在全国范围内推广。</h5>
        <h5>2004年5月28日，唐亚伟教授九十岁生日时，国际速联名誉主席凯勒尔和当届主席拉曼德利专程前来祝贺，并代表国际速联授予唐老终身荣誉奖。</h5>
        <h5>2005年7月，在第45届国际速联大会上，亚伟中文速录机一展风采，引起了国际速录界同行的特别关注。</h5>
        <h5>2006年5月9日，在北京市科学技术大会上，唐亚伟教授发明的亚伟中文速录机荣获“北京市科学技术一等奖”，北京市市长王琦山亲自为唐老颁发奖杯和证书。</h5>
        <h5>2006年5月，国际速联主席迦瓦希柯先生专程来华与北京市速记协会签署了合作意向书，旨在开发欧洲的多语种亚伟速录机。</h5>
        <h5>2006年11月22日，鉴于唐亚伟教授发明的亚伟中文速录机，及其在促进电脑速录行业兴起方面所做出的卓越贡献，中国中文信息学会特将首届“钱伟长中文信息处理科学技术一等奖”授予唐亚伟教授，这一奖项也是我国中文信息处理科技领域的最高荣誉。</h5>
        <h5>2007年2月27日，亚伟中文速录机荣获国家技术发明二等奖。在颁奖大会上，国家领导人向唐亚伟教授颁发了奖状和证书，国家主席胡锦涛同唐老亲切握手并热情询问唐老高寿。</h5>
        <h5>2009年8月15-21日，第47届国际速联大会在北京召开，唐亚伟教授宣布大会开幕。这是国际速联大会成立以来首次离开欧洲到亚洲举办大会，取得圆满成功。我国选手获在比赛中获得多项冠军及奖牌总数第一的卓越成绩，向国际展现了我国速记的水平，标志着我国速记处于国际领先地位。这是唐亚伟教授发明的中文速录机将我国速记推向新的高峰。</h5>
        <h5>2011年7月，巴黎第48届国际速联大会召开，教育委员会出版国际速联第一本论文集，标志着中国速录教育研究走在世界前列。</h5>
        <h5>2013年7月，在比利时根特第49届国际速联大会上，中国入选国际速联核心决策领导机构——5人执委会。</h5>
        
