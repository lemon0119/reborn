        <h3 style="text-align: center;">亚伟中文速录机概述</h3>
        <h5>1993年，著名速记专家、现年92岁高龄的唐亚伟教授发明了亚伟中文速录机。北京晓军办公设备有限公司集各学科专家数十人，秉承自主创新的精神，没用国家一分钱，耗巨资将其研制成功。亚伟中文速录机的正常输入速度为每分钟220字，极限速度则达到每分钟520字。</h5>
        <h5>1994年通过劳动部职业技能与技术鉴定。专家一致认为该产品攻克了汉字录入速度与语言同步的难关，填补了我国实时听打领域的空白。</h5>
