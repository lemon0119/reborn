        <h3 style="text-align: center;">亚伟中文速录技术及装置</h3>
        <h5>荣获国家技术发明二等奖的亚伟中文速录技术是进行实时语言信息采集的关键技术，它采用专用键盘和专用软件对语言信息同步记录并转化为文字，是中文信息处理必不可少的重要环节，是实现信息化的基础。该项目的深入研发和成功应用，标志着我国中文信息处理技术实现了质的飞跃，与信息化的国际大潮成功接轨。特别是在信息采集的速度与效率上，还取得了优于国际同行的骄人业绩，是中文信息处理发展高度的新里程碑。</h5>
        <h5>亚伟速录技术已经广泛应用于国家领导人的出访、高访会谈记录，国家机关及企事业单位日常办公记录，各种会议记录，法院庭审记录，新闻采访记录，电视字幕、场记、录音整理记录，网络直播记录，口头创作、翻译记录，口授文件记录等。数以万计掌握速录技能的专门人才活跃在人大、外交部、法院、大中型企业及各种会议现场，形成了一个速录从业人员群体。2003年，劳动和社会保障部组织颁布施行了《速录师》国家职业标准，标志着一个由亚伟速录技术创造的崭新职业的诞生。目前，一批批掌握亚伟速录技术的人才成功就业，为社会开拓了一个新的就业途径和岗位。</h5>
        <h5>与此同时，各大院校相继开设了亚伟速录课程，在秘书、英语、法律、新闻等相关专业增加这项技能。近年来，这些大中专院校已培养出许多熟练掌握亚伟速录技术的优秀毕业生，他们在秘书等工作岗位中发挥出重要的作用，使我国的秘书人员“回归”到熟练掌握速记技能的时代，他们能够在第一时间记录领导口述、企业内部会议，在竞争日趋益激烈的经济环境下，大大提高政府、企业的工作效率，更好地发挥出秘书“参与管理事务”的功能。</h5>
        <h5>亚伟速录巧妙地将中文速记技术与计算机技术有机地结合，创造性地设计了一击可录入两个汉字的专用键盘，独具匠心地仅用11个字母表示全部汉语拼音，同时结合人体工学、运动生理学和心理学的原理，采用“弹钢琴”般的击键方式，科学合理地实现了低强度下的高速度，成功突破了语言信息采集的瓶颈，做到“语音落，文稿出”，是国内首创并达到国际先进水平的划时代专利技术，并荣获中文信息处理技术一等奖。</h5>
        
