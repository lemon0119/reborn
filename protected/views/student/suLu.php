        <h3 style="text-align: center;">第一台中文速录机的诞生</h3>
        <h5>在上世纪五十年代的中后期，唐亚伟感到手写速记的简单符号来记录语言，最大的障碍就是在练习每个笔划之间的大小比例的时候产生了巨大的困难。当时在世界范围内，普遍流行一种机械速记，唐亚伟也打算从这方面研究。由于当时获得国外的资料极为有限，他基本上是靠着自己一点一滴地对过去一些机械速记的原理的不停断的研究，几乎到了着魔的境地。当时各种打字机，可以说只要有键盘图的打字机，都做过专门的编码，但是由于当时打字机极为昂贵，买不起，只能每次创造一个方案以后自己用键盘图悄悄地练。算到93年的亚伟中文速录机的研制成功，近三四十年的时间里研究出的方案不下一百种，如果把方案从地上摞起来，可能比唐亚伟自己还要高。</h5>
        <h5>中美建交后，美国司法代表团到中国访华，和我们司法界交流，他们带来了速录人员，想同中国的速录人员交流，但是当时中国的法院里头没有速录员，都是用手写笔记，他们感到很失望。后来来访的客人回去以后，在一篇报纸上写了一篇文章，说他们在中国没有找到交流对象。同时，美国速记协会也给当时担任北京市速记协会唐亚伟教授来函说明了情况。那些有关中国速录业落后的言论深深地刺痛了唐老先生，他给最高人民法院写信，建议法院在庭审记录时采用“速记”手段，最高法院很重视，当即回函表示如果能够研制出当场出汉字的速记机，法院系统将首先采用。于是古稀之年的老先生重新找出工具、资料开始钻研，又先后摸索了三、四十套方案。</h5>
        <h5>进入九十年代以后，计算机开始进入国内。自从了解了电脑以后，唐亚伟一直在想办法，如何把速记和电脑结合，把最复杂、最繁杂的工作交给电脑。正因为速记后期“翻译”最累，导致了速记很少有人在用。那么，普及速记的最好方式非电脑莫属。然而，对于一位对电脑一窍不通的老人而言，这个难度是可想而知了。</h5>
        <h5>唐亚伟首先需要解决的，就是速记能不能按照拼音完全打出来。当时，他创造性地设计了一个输入方案，利用标准键盘一键一击，一只手打声母、另一只手打韵母。由于汉字基本都是声韵结合的，两只手正好声、韵分工。按照这个设想，每分钟应该能够输入200字。但是，这样一来两只手太忙了。不过，唐亚伟发现，汉语里面双音词最多，怎样利用这种双音词的特色，开发便捷快速的电脑速记方法？</h5>
        <h5>唐亚伟从钢琴演奏上受到启发，既然弹钢琴可以一只手弹主旋律，另一只手弹和弦，甚至可以交叉弹奏，那么电脑速记完全也可以采用双手同时敲击键盘输入的方法。因为两只手同时敲击键盘，记录的速度自然能够明显提高。但是，这个想法引发的下一个问题是，必须抛弃现有的计算机键盘，重新研制一种新型的速录键盘。凭借深厚的速记功底、丰富的编码能力和扎实的拼音功夫，唐亚伟重新对键盘进行设计。面对26个字母，声码需要用几个键、韵码需要用几个键、按键相互之间的关联等等，成为他每天琢磨的重点。1993年，唐亚伟终于拿出了一套完整的电脑速记编码方案和速录键盘设计。</h5>
        <h5>但是，计算机速记在当时计算机还很不发达的情况下，让别人听起来是那样的陌生，与人合作开发的事宜一再搁浅，后来才找到志同道合的合作公司。</h5>
        <h5>直到1994年，经过了几百个日夜，速录机终于在北京展览馆最显著的展台前亮相。30分钟的演讲后，与会者在出门的时候，每人可以得到一份热腾腾的快报，这就是用速录机来进行实时记录、瞬间完稿，用速印机把一张张的快报印刷出来的。这无疑是国内拿到的最快的小报了。</h5>
