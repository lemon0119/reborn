    <h4 style="text-align: center;">比赛</h4>
    <h5 >大会如期举办了各项比赛，同时还增加了中国特色比赛。这是本届大会唯一一个全部参赛选手均来自同一个国家（中国）的比赛项目。
    第一项“行进打”：选手们将亚伟速录机挂在身上，边走边速录。
    第二项“盲打”：每位选手戴上眼罩，完全不看键盘和屏幕，好象视障碍人士在速录。
    第三项“极限看打”：比赛文章事前发给选手进行训练，比赛现场，选手们努力用最快的速度打完。
    这次，我国还组织了4名小学生参加速记比赛，是国际速联比赛年龄最小的选手，充分体现了我国对速记教育从孩子抓起的重视程度，为世界各国速记教育作出了榜样。</h5>

