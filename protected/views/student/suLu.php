        <h3  style="text-align: center;">唐可为电视台采访记录</h3>
        <h5>提问：唐老师，您的家庭应该属于是有传承的，您父亲做了一辈子速记工作，您简单介绍一下唐老先生的情况。</h5>
        <h5>唐可为：我父亲一生都在搞速记。他十几岁的时候，在读高中的时候，因为他家庭很贫穷，读大学的时候，他给校长作秘书，勤工俭学。作秘书工作需要为老师书写一些东西，他觉得汉字书写太复杂，就想能不能有一个更好的方法，那时候初步开始研究速记。20多岁的时候，自己发明了一个速记方法，叫亚伟速记。那时候是抗日战争时期，1946年，他认为他自己掌握了速记还不够，他想让更多的人掌握这项技术。这项技术是很重要的，因为人们有那么多的讲话内容，用汉字书写太慢了。于是他开始办学。抗日战争时期天天有飞机轰炸，当时有很多爱国人士都集中在重庆，提出了很多救国思想，教育救国思想、实业救国思想等等，他提出来一个思想，速记也可以救国。他把速记提到这么高的层面上，自然而然，他一生就离不开速记了。</h5>
        <h5>他在推广速记的时候，受到了冯玉祥将军的支持，冯玉祥将军非常重视速记，他认为速记是每一个办公室文案人员都应该掌握的技术。冯玉祥将军德尊望重，出任亚伟速记学校的董事长。也就是从1946年，我父亲就完全投入到速记这个行业中来了，一干就是一生，然后抗日战争胜利以后，他就到上海办学校，当时请来了中国很著名的教育家，陶行知、李公朴，都是亚伟速记学校的校长。当时在上海提出了很多“人人速记”、“人人识字”的口号，《大公报》中几乎天天可以看到有关速记的稿件、文章、广告，甚至，人民大学语言文字研究所的所长说，我当时是解放军进上海城的时候，给我印象最深的是亚伟速记，所以他解放以后他一直在找唐老，他就说，我进上海的时候，给印象最深的就是亚伟速记。他当时是作为八路军的文化干部，他就想我要是学会速记，在解放军中做工作多方便啊。后来，他作了语言文字研究所所长以后，特意请我父亲出任他的研究所顾问，所以我父亲一直是人民大学语言文字研究所顾问。</h5>
        <h5>速记这一生给我父亲带来了无穷无尽的财富，为什么呢？到今天，他只要谈到速记嘴就会停不下来了，他已经91岁了，他身体非常健康，现在也能够完全自理。另外，他所有的子女都很尊重他，因为他创造了一个速记的事业，我们所有子女都很尊重他。另外，在社会上，提到唐老，大家都非常尊重，很多人都把亚伟这个名字当做速记的代名词，说亚伟就是在说速记。</h5>
        <h5>给我印象最深刻的是，我父亲有一次骑自行车不小心被一个小姑娘撞倒了，脑部亚型出血，慢慢就不省人事了，在他晕倒之前的一段时间，他把所有的人，所有的事务都称为一个词“速记”，喝杯水也是先说速记，然后再说“喝杯水”，见到我，一般是叫我“可为”，这时候见我就说“速记”，然后笑笑，摇摇头，说你不是速记，你是可为，可见速记已经融入他的每一个细胞中。速录这项技术我们也搞成功了，这是一个计算机的技术，速录机。他今天的健康也来源于他事业的成功。我经常跟我父亲调侃，您知道您现在为什么身体这么棒吗？现在打速录机的人，都不叫打速录机，说打亚伟，而且当着我的面就这么说，我问他你干什么的？打亚伟的。我说打亚伟就是打你，如果你身体不结实，经得住这么多人打吗？所以越打越结实。</h5>
        <h5>主持人：唐老先生把自己的一生和精力都给了速记事业，把名字也献出去了。计算机速记称为速录，工作原理，是不是也是您的父亲创造发明的？</h5>
        <h5>唐可为：是。手写速记是用简单的符号来记录汉字，简单的符号是表音符号，就是把声音记录下来，这个符号记录完以后必须要翻译，如果不翻译，速记稿就没有意义。一个小时的记录，需要四到十个小时的翻译时间，速记工作者太辛苦，他想摆脱速记工作者辛苦的工作，用什么办法呢？手写速记用手写，就有可能刚开始写得比较准，写到疲劳了，就写不准了，另外讲话慢的时候写得准，讲话快的时候写不准，写不准太正常了，怎么能一下子就能写准，因为写准才能好翻译。所以他曾经想过机械速记。在国外也是这样的一个阶段，手写速记、机械速记、计算机速记。中国有一个六十年代的文化大革命，基本上把速记荡涤干净了。</h5>
        <h5>他一直认为，如果推广机械速记，就能够把速记一下子打准。研究机械速记，他研究的资料摞起来有我一人这么高，不知道研究了多少方案。这么多方案以后，计算机时代来临了，正好文化大革命结束了，国外也开始进入计算机速记阶段，这时候他就把机械速记和电脑速记结合在一起，再重新研究。计算机是新兴的一项技术，不要说对一个八十岁的老人，对我们这样的年轻人来讲，我们对计算机也是知之甚少。在计算机研究的时候，只要是有机会能够了解计算机相关的知识，他一定去搜集，骑着自行车到处跑，那时候有文字处理机，他把我们家仅有的一万块钱去买了一个文字处理机。他说我买一台机器不是为打字，是为了研究打字。其实这个打字的键盘跟普通打字用的键盘是完全不一样的，他就是要研究为什么普通键盘打不快，要想打快怎么办？他反复在普通键盘上研究，最后决定要开发出一个新的机器。这样就把速记机拿出来了，如果能够研究出一个特殊的键盘，打字的速度就大大提高了。</h5>
        <h5>所以说，一个发明，不是一夜之间突发奇想研究出来的，他是研究了很长时间的。另外，他对语言学驾驭能力很强，因为符号是表音的嘛，他对语言研究很多，词频、音频、拼音的规律，哪个用得多，哪个用得少。他在研究速录机的时候也把这些知识融合在里面，可以合理分配每个手指的运动速度，用最灵活的手指干最多的活，最不灵活的手指干最少的活，五个手指都发挥作用，就能够打快了。到今天为止，很多学员通过学习亚伟速录以后，跟我说，你们老爷子怎么想的？绝了！有速记编码的工作和功底，他总结出很多规律来，不是说简单就好，还要便于记忆。</h5>
        <h5>我们的速录机一共有24个键，从中间分开，12个键要打出所有的汉语拼音来，严格来讲，越好打的键位应该优先考虑，但是不光是要考虑这一点，还要考虑规律。一定要让学习者能够寻求一个规律，自然而然就可以学习，这样学习起来非常简单。不是说这样好打，就强行进行规定，这样毫无规律可言，给学习者造成很大困难，而且他勇于放弃。他把食指的两个键，居然设计成删除键，这是很多人难以想象的，因为食指打字是最好的，为什么要删除呢？他说，删除是任何一个输入方法都要做的工作，打错了就要删掉重新打。如果用别的手指来打删除键就有可能误打，只有最灵巧的手指不容易误打。所以他把最好打的键位当成删除键，这是根据速记学等多方面的功底研究出来的。还有拇指，普通键盘是作空格用的，因为拇指运动不是很灵活。经他研究，他认为不是这样的，中指、食指，其次就是拇指，所以把拇指放在一个重要的位置，标准键盘上拇指就是空格键，干一个事情，在速录机的键盘上就把拇指充分利用起来了。看似一个简单的发明创造，实际上是集几十年的功夫发展起来的，尤其是跟计算机结合这方面。</h5>
        <h5>提问：编码是老爷子毕生的心血凝聚而成的，把这个理论变成实际能看到的东西，也就是速录机，这个工作由谁来做的？</h5>
        <h5>唐可为：理论研究的时候我父亲就有所准备。我在中学阶段的时候，他就搞手写速记方案。我在初中的时候，他就写手写速记的符号，那时候，手写速记是密码，谁也不敢公开教学，所以她就拿我作实验。我那时候比较听话，天天练，他永远在改方案，我总要重新学习，到高中的时候，我就练烦了，他搞计算机研究的时候，他说，计算机我不懂，你帮我搜集计算机知识，他做编码的时候，经常叫上我，不让我练了，让我和他共同研究。这时候我就和他坐在一起共同研究。编码上，完全是我父亲的功底，在这方面，我的作用不大。真正到了键盘上面，键盘多长，多短，多高多短，之间的距离，怎么呼应，这时候已经超出速记的范畴之外了，有人体工程学、人体力学方面的知识。我有一些朋友，计算机圈内的朋友，请他们帮我搜集相关的资料，共同来研发。但是初期的研发只是纸上谈兵，在纸上谈这个理论是否可行。比如说我们讨论过一个问题，双手多键并击，每个手各干各事，一只手可以打出一个汉字，双手并击可以打出两个汉字来，一心两用，能不能成功？这个问题我们当时讨论得很激烈。左手走直线，右手画圈，大家经常搞这样的实验。当时我父亲也在想能不能成功。我父亲有一次看钢琴演奏会，看到半截他离开了，他回去继续研究，他找到信心了。为什么呢？钢琴是典型的多键并击，另外，双手绝对是在各干各的事，而且甚至双手还可以交换，这就证明，只要训练就可以达到。还有一次，春节联欢晚会，有一个人在表演左右书法，双手写不同的字。当时主持人问，你的水平太高了，一只手写就很难了，双手各干各的，太难了。他说，不难，只要你练，就可以成功。这就证明，只要训练，就可以成功。在这上面的研究上，我父亲花了这么多工夫，也加上家人的帮助，共同把这项技术研发成功。</h5>
        <h5>提问：您是非常情愿做这个事情，而且现在已经有了自己的事业？</h5>
        <h5>唐可为：研究的时候我是很自愿的，老父亲的事情，需要我帮助，我肯定要尽力帮助。但是一旦要上这个项目，找到投资了，他就跟我谈，能不能帮他主持这个事情？这就有矛盾了。因为我做了一个文化交流中心的项目，我们几个人，白手起家，已经小有规模，有收获了，突然间他让我做速记，我对速记太了解了，速记风风雨雨几十年，一分钱也没有挣到，我就说，您再让我搞，我这一辈子是不是也要陷进去？再有，速记的圈子终归太小，是能够起到很大的作用，孙中山的《三民主义》就是速记稿完成的，英国的多产作家巴巴拉也是用速记人员记录，口述写书，这个事情我也清楚。但是让我做这个项目，中国完全是一张白纸，做这个事情很难。而且这个范围可能就是法院庭审记录，会议，当时我们也接到美国法院来的信，说法院书记员每分钟书写汉字速度50个字，说话二百多个字，也有这样的信过来，在法院做当然也很有作为。但是目前是什么事情都是零，做起来很难，我那边也不错，所以我就搪塞他，跟我说我也聊，但是没有上心。
    当时我父亲天天去跑，找投资，这么多年纪，每天奔波于中关村、高校之间，找他的老朋友，那时候他已经快八十岁了，他的老朋友也都是七八十岁的人，要资金没有资金，要权利也没有权利，只是说好，但是无能为力。当时他的精神很感人。真正走上速录这条道路，还是我父亲出车祸那一个事情。那次我太感动了，那一次，他差点就没命了，在那样一个时刻，满脑子就是速记，连儿子都是速记，我们作为儿女再不帮他做速记，还做什么？这件事是一个很重要的突发事件，我一下子就感觉我有责任把这个事情做好。</h5>
        <h5>另外，还牵扯到这个技术的保护问题，我父亲发明这个技术以后，我们认为这个技术改写了历史，但是一旦公布出去，人人都可以做，只要公开了，大家都可以做。我们想把这项技术保护起来，就想申请专利，但是申请专利之前我们想做一些实验，实验谁来做？只有我来做。他八十岁的人了，怎么能做这个实验呢？第一期实验班就是我开起来的，就是为了把这个专利保护起来。我们经过了一段测试以后，投资方和教研机构都认为这项技术毫无问题，从理论上证明是可以的，实践上也是可以的，所以就报了国家专利，这时候我就深深地陷进来了。当时我看到我那一批学生太可爱了，我们那些小姑娘第一次学习速录的时候，我们给他们的是一张纸片，这个纸画着键位，这个纸打了一周，我们这些学生毫无怨言，老师要求怎么做就怎么做。后来我们就做了木头的键盘，把键芯钉在木头上，按。我当时和费教授，两个人进行教学，我们是晚上进行研究，明天教什么，然后第二天上课，我们学生很多时候拿到的都是手写教材，就是我们昨天晚上备的课。我们从93年9月份开始搞培训，中间申办专利，同步又招聘软件人员开发，招聘硬件人员开发速记机，到2004年5月19日，我们的速录机就研发成功了。成功的那台速录机，还不是完全意义上的第一代速录机，那时候模具还在设计阶段，就找了一个金属加工厂，做了一批铁壳的键盘，这个键盘我们现在还保留着，作为文物了。木头键盘、纸键盘，我们都保留着。</h5>
        <h5>提问：那时候，大家是不是都感觉很兴奋？</h5>
        <h5>唐可为：我们的过程是很艰难的，第一步，见着编码，我们就用了四个月的时间。小姑娘在哪儿打字，正确与否我根本不知道，我得看着她的手，这个编码应该拇指下去，小拇指下去，小拇指没有下去，我就说你打错了，注意小拇指，是用肉眼来看正确与否。到什么时候见到编码了呢？四个月以后。我们一步步兴奋，最后见到速录机以后已经不是那么强烈了，因为不是一夜之间出来的，是一步步来的。能打出编码来，打的每一下我都有纪录了，我根据编码来看，是否打错了，那时候经常做校对工作，每天学生打了大量的编码，根据编码来看正确与否。5月19号前一个月，汉字才真正出来。</h5>
        <h5>提问：您那时候可能还有一项重要的速录机推广工作，让社会认可您这个技术，是不是？</h5>
        <h5>唐可为：1994年5月19号，北京计算机交易展示会，我们在中央展台上，隆重推出亚伟中文速录机，当时花了三十多万。大家见到亚伟速录机都很惊奇，我们搞了一个现代会议厅，你现场发言，我们现场大屏幕投影，而且你走的时候，可以拿走你的发言稿，我们当时用速印机给你当场印出来，叫晓军快报，是中国最快的报纸。这是94年的时候。到了96年，我又参加计算机展示会，我们租了一个小展位，我听到的声音就是“这东西还活着呢！这个打击太大了。我们从94年推广以后，我们走哪儿演示效果都非常好，没有人说它不好。但是设备没有人买，为什么？因为当时的计算机，一台386还上万，买一台速录机，再买一台计算机，而且很多人不会用计算机，我学会了速录机，计算机我还不懂，这就比较麻烦。另外，速录机买回来以后，谁用？领导说，我都有稿子，我用速录干吗？这样我们就找没稿子的，我们就想在速记服务上发展一下，我们终于找到一个单位，这个人发言没有稿子，而且是一个大会，我们就说可以做现场记录，把现场发言记录下来，这个领导一听，马上说不行，说我这次会议没准备，如果我讲错了，你记走了，那我怎么受得了？当时我非常灰心。从93年立项，我们投入大量的精力，孩子们一天天进步，我父亲的心血一天天有成果，94年做演示会，到真正的机器出来，没想到社会这么一个反响。夸的大有人在，买的人一个没有，这项技术没有人使用。最后，在1996年的时候，不瞒您说，我们从开始立项投资，到1996年我们投资了六百多万。这么大的费用投进去，几千台速录机放到仓库里，没有任何效果。我们这时候就处于生与死的边缘。我们当时在中关村肯德基楼上，是一个非常优越的位置，几乎进中关村，必经我们公司的门口，是一个中心地带。当时我还跟我们很多员工在调侃，说什么时候亚伟速录机卖得跟肯德基一样火了，那怎么就不得了了。但是调侃是调侃，现实是现实，在1996年的时候，可以这么说，整个公司帐面上就剩下60块钱了，连员工工资都发不出来，房费也付不起了，最后我们撤出中关村。在撤出的时候，公司的软件开发人员、硬件开发人员、广告营销策划人员，当时这个公司将近一百个人，我们撤出的时候就剩下五个人。</h5>
