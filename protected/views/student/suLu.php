        <h3 style="text-align: center;">第47届国际速联大会概况</h3>

        <h5>第47届国际速联大会及比赛于2009年8月15-21日在北京国家奥林匹克中心举行，16-17日为比赛。其间还安排了教育论坛、学术讲座、青年日、展览、宴会等，并于20日晚间胜利闭幕，21日安排长城一日游。
    这是该组织百余年以来第一次离开欧洲到亚洲举办年会及国际比赛，来自世界各地26个国家的546位业界知名人士、学者和选手汇集首都，渡过了丰富、紧张而又愉快的7天。</h5>

        <h4 style="text-align: center;">欢乐的大会</h4>
    <h5 >大会以增进世界各国信息处理和速录领域相关人员的相互了解和友谊为宗旨，自始至终洋溢着欢乐与和谐。东道主的热情与勤奋鼓舞和感染了每一位与会者，各国代表在京的感受是愉快的，团结、友谊与欢乐构成了本次大会的主旋律。</h5>

        <h4 style="text-align: center;">成功的大会</h4>
    <h5 >大会顺利地完成了所有预定的计划，各项活动紧张而又有序。开闭幕式隆重而热烈；比赛公平公正；学术活动内容丰富。一切都按部就班地进行，没有出现安全保卫、公共卫生事故及任何群体事件，妥善处理了个别突发事件，圆满完成预定任务。</h5>

        <h4 style="text-align: center;">丰富的成果</h4>
    <h5 >大会的重头戏是学术交流和比赛，这两项活动都取得了丰硕的成果。共有中外学者19人在大会上做学术报告、主题演讲；成立了以中国人为主席的国际速录教育委员会；中国选手在比赛中优异成绩，获得16项冠军及奖牌总数第一，中国速录从此全面跨入世界先进行列。同时，中国还与美、法、日、韩、捷克、喀麦隆等国进行了速录技术、教育与产品交流、合作洽谈等，就今后的合作交换意见，初步达成一致。</h5>

    <h4 style="text-align: center;">注册情况</h4>
    <h5 >2009年2月25日凌晨，国际速联官方注册正式开通，当年即有20人注册；2009年6月15日国外注册截止，7月15日，全部注册截止，共有546人注册，其中中国注册选手108人。</h5>

    <h4 style="text-align: center;">开幕式</h4>
    <h5 >2009年8月15日下午3点38分，国际速联终身荣誉奖获得者、我国速记泰斗、九三学社社员、亚伟速记和亚伟中文速录机的发明人、九十五岁高龄的唐亚伟教授在观众热烈的掌声中，手举小会旗缓缓起身，声音洪亮地宣布“第47届国际速联大会开幕”，标志着历经三年精心筹备的“国际速记奥林匹克”大会正式拉开帷幕！
    全国政协常委、副秘书长、九三学社常务副主席陈抗甫先生，全国人大信息中心主任王平先生等领导嘉宾到会祝贺。中国工程院院士、中国中文信息学会理事长倪光南先生致欢迎辞，国际速联秘书长保罗先生致答谢辞，中国前驻纳米比亚大使、外交部前档案局局长廉正保先生和中国残疾人联合会就业指导中心副主任李强致贺辞。国际速联主席唐可亮先生代表国际速联中国代表团和本次大会组委会向领导、嘉宾和代表们发表了热情洋溢的讲话，感谢大家的光临。
